<p>No todo el mundo se va de vacaciones a la vez. Y los que se quedan en Madrid, puede que echen de menos un Madrid.rb.</p>

<p>Para eso existe el Terracismo.rb.</p>

<p>En una reunión normal de Madrid.rb hay una charla dada por algún ponente, seguida de unas preguntas, y luego charlas con comida y bebida, mayormente cervezas.</p>

<p>Terracismo.rb es más minimalista; no hay charla ni ponente, vamos directamente a las cervezas. Todos son bienvenidos. Juntemos nuestros jarros de cerveza y llamemos al Capitán Planeta, para que nos haga una tarde digna del mejor terracismo.</p>

<h2>
<a id="user-content-apúntense" class="anchor" href="#ap%C3%BAntense" aria-hidden="true"><span class="octicon octicon-link"></span></a>Apúntense</h2>

<p>Apuntarse es completamente opcional y no obliga ni da derecho a nada. Pero nos ayuda en la organización de los eventos. (Por ejemplo, si mucha gente se apunta a un terracismo tendríamos que avisar al local). </p>

<p>Si tienes pensado venir, <a href="https://github.com/madridrb/madridrb.github.io/wiki/2014-07-31:-Terracismo.rb/_edit">edita esta página y añádete a la lista</a> . Puedes usar tu nick the github, tu handler de twitter, o cosas alocadas como tu nombre.</p>

